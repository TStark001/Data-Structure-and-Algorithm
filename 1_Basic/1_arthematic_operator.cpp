#include <iostream>
using namespace std;

int main()
{
    int a = 6;
    int b = 3;
    cout << a + b << endl; // 9
    cout << a - b << endl; // 3
    cout << a * b << endl; // 18
    cout << a / b << endl; // 2
    cout << a % b << endl; // 0
    // '%' :- this is modulus operator which give the reminder.

    cout << (a == b) << endl; // false
    cout << (a != b) << endl; // true
    cout << (a >= b) << endl; // true

    return 0;
}
// "==":-- equal to equal to opertor which will compare the give value.
// "!=":-- not equal opertor.
// ">=":-- greater than equal to operator.
// "++":-- increament opertor which will add +1 to the int.
// "--":-- decreament opertor which will subtrate -1 to the int.
