// Print "odd" if the input value is odd, otherwise print "even".
#include <iostream>
using namespace std;

int main()
{
    int n1;
    cout<<"Enter your number:";
    cin>>n1;

    if(n1%2==0){
        cout<<"This number is Even."<<endl;
    }
    else {
        cout<<"This number is Odd."<<endl;
    }
    return 0;
}

