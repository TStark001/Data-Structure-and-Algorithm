#include <iostream>
using namespace std;

int main()
{
    int a = 6;
    int b = 3;
    a += 3;
    b -= 2;
    cout << a << endl; // 9
    cout << b << endl; // 1
    return 0;
}
// Assigment operator:
// "+=" :---- a+=1 ---> a=a+1
// "-=" :---- a-=1 ---> a=a-1
// "/=" :---- a/=1 ---> a=a/1
// "%=" :---- a%=1 ---> a=a%1